var CACHE_NAME = 'offline-support';

self.addEventListener('install', function(event) {
  self.skipWaiting();
  event.waitUntil(caches.open(CACHE_NAME).then(function(cache) {
    return urlsToCache.map((url) => cache.add(url).catch((err) => console.log(err)))
  }));
});

self.addEventListener('fetch', function(event) {
  event.respondWith(
    caches.open(CACHE_NAME).then(function(cache) {
      return cache.match(event.request,{ignoreVary: true}).then(function (response) {
        var req = fetch(event.request).then(function(response) {
            if(!(new URL(event.request.url)).search){
              console.log(`cache UPDATE for ${event.request.url}`)
              cache.put(event.request, response.clone());
            } 
            return response;
          });
        if (response){
            console.log(`cache HIT for ${event.request.url}`)
            return response
        }else{
            console.log(`cache MISS for ${event.request.url}`)
            return req
        }   
      });
    })
  );
});

self.addEventListener('message', function(event) {
  if (event.data) { 
    let data = event.data;
    caches.open(CACHE_NAME).then(function (cache) {
        cache.add(data)
    })
  }
});